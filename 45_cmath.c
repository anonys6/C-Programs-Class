#include <stdio.h>
#include <math.h>

int main()
{
    printf("exp(2) = %f\n", exp(2));
    printf("sqrt(25) = %f\n", sqrt(25));
    printf("fabs(-34.89) = %f\n", fabs(-34.89));
    printf("abs(-45) = %d\n", abs(-45));
    printf("floor(45.82) = %f\n", floor(45.82));
    printf("ceil(32.25) = %f\n", ceil(32.25));
    printf("pow(2, 5) = %f\n", pow(2, 5));

    return 0;
}
